    private void drawNodes() {
        DefaultTableModel tableModel = new DefaultTableModel(
            new String[]{"Name", "X", "Y", ""
            		+ "Weight"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column
            ) {
                return false;
            }
        };

        for (Node node : nodes) {
            Object[] row = new Object[4];
            row[0] = node.getName();
            row[1] = node.getX();
            row[2] = node.getY();
            row[3] = node.getWeight();

            tableModel.addRow(row);
        }

        jTable1.setModel(tableModel);
        jTable2.setModel(tableModel);
    }

    private void drawLinks() {
        DefaultTableModel tableModel = new DefaultTableModel(
            new String[]{"Start", "Direction", "End", "Length"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column
            ) {
                return false;
            }
        };

        for (LinkDescription link : linkDescriptions) {
            Object[] row = new Object[4];
            row[0] = link.nameA;
            row[1] = LinkDescription.getDirectionName(link.direction);
            row[2] = link.nameB;
            row[3] = link.length;

            tableModel.addRow(row);
        }

        jTable4.setModel(tableModel);
    }

    private void redraw() {
        drawNodes();
        drawLinks();
    }

