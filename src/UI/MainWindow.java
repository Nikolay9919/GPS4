    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int a = jTable1.getSelectedRow();
        int b = jTable2.getSelectedRow();

        if (a == -1 || b == -1) {
            JOptionPane.showMessageDialog(
                null,
                "Select 2 nodes!",
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        float length;
        try {
            length = Float.parseFloat(jTextField5.getText());
        } catch (java.lang.NumberFormatException e) {
            showParseException();
            return;
        }

        String nameA = (String) jTable1.getModel().getValueAt(a, 0);
        String nameB = (String) jTable2.getModel().getValueAt(b, 0);

        Direction direction = null;

        if (jRadioButton1.isSelected()) {
            direction = Direction.RIGHT;
        }

        if (jRadioButton2.isSelected()) {
            direction = Direction.LEFT;
        }

        if (jRadioButton3.isSelected()) {
            direction = Direction.BOTH;
        }

        LinkDescription lnk = new LinkDescription(nameA, nameB, direction, length);

        Optional<LinkDescription> same = linkDescriptions
            .stream()
            .filter(l -> l.nameA.equals(nameA) && l.nameB.equals(nameB))
            .findFirst();

        if (same.isPresent()) {
            linkDescriptions.remove(same.get());
        }

        linkDescriptions.add(lnk);
        redraw();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String nameA = jTextField6.getText();
        String nameB = jTextField7.getText();
        if (nameA.length() == 0 || nameB.length() == 0) {
            showParseException();
            return;
        }

        Graph g = new Graph();
        for (Node node : nodes) {
            node.isTested = false;
            g.graph.put(node.getName(), node);
        }

        for (LinkDescription linkDescription : linkDescriptions) {
            String a = linkDescription.nameA;
            String b = linkDescription.nameB;

            Optional<Node> nodeA = nodes.stream().filter(n -> n.getName().equals(a)).findFirst();
            Optional<Node> nodeB = nodes.stream().filter(n -> n.getName().equals(b)).findFirst();

            if (linkDescription.direction.equals(Direction.RIGHT)) {
                g.createPath(nodeA.get(), linkDescription.length, nodeB.get());
            }

            if (linkDescription.direction.equals(Direction.LEFT)) {
                g.createPath(nodeB.get(), linkDescription.length, nodeA.get());
            }

            if (linkDescription.direction.equals(Direction.BOTH)) {
                g.createTwoWayPath(nodeA.get(), linkDescription.length, nodeB.get());
            }
        }

        switch (jComboBox1.getSelectedIndex()) {
            case 0:
                g.search(nameA, nameB, new WeightSearch(g));
                break;
            case 1:
                g.search(nameA, nameB, new SearchByCoordinates(g));
                break;
            case 2:
                g.search(nameA, nameB, new BreathSearch(g));
                break;
        }

    }//GEN-LAST:event_jButton3ActionPerformed

